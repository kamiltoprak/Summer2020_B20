package ReplitTry;
/*use a for loop to print numbers from 100 to 0

        example:
        100
        99
        98
        ...
        3
        2
        1*/
import java.util.*;
public class R067_Loop_CountDown {

        public static void main(String[] args) {
            for(int i=100; i>0;i--){

                System.out.println(i);
            }



        }
    }

