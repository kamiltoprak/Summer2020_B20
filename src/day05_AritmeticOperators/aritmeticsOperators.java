package day05_AritmeticOperators;

public class aritmeticsOperators {
    public static void main(String[] args) {
        System.out.println(20+3-2+1);
        //23-2+1 =22
        System.out.println(20*3);// mutiply  numbers
        System.out.println(2+3-1*4);// calculate first  multiplacation than  plus and minus
        System.out.println(10/3); // if both number is integer result will  be integer -int/int =int
        System.out.println(10.0/3);//  double result becouse one of the number is decimal - double/ int = double
        System.out.println(10/3.0);// one of the number is decimal  rsult will be decimal int/double= double
        System.out.println(10%4); // 10/4  mean integer left over 2  and result will  whole number 2 int%int = int
        System.out.println(10.0%4);
    }
}
