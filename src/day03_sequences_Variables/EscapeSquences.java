package day03_sequences_Variables;

public class EscapeSquences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \n\t\\My name is Cybertek");
        System.out.println("\tI  am in the Pittsburgh\n ");
        System.out.println("\n\n\t\t\tI Love Programing");
        System.out.println("\\\\\\");
        System.out.println("/");
        System.out.println("My name is \'KAMIL\'"); //'Kamul'
        System.out.println("My  name is ' KAMIL ' ");//'Kamil'
        System.out.println("My favorite  TV  series: \"GAME of Thrones\" ");

    }
}
